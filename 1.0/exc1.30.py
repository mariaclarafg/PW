# Crie dois conjuntos e combine-os usando as operações de união, interseção e
# diferença, apresentando os resultados de cada operação. 

conjunto_1 = {10, 20, 30, 40, 50}
conjunto_2 = {5, 10, 15, 20, 25}

print(conjunto_1 | conjunto_2)

print( conjunto_1 & conjunto_2)

print(conjunto_1 - conjunto_2)