# Peça ao usuário para digitar o raio de um círculo e calcule a área e o comprimento
# do círculo. 

raio = float(input("Digite o raio do círculo:"))

area = 3.14 * (raio ** 2)
comprimento = 2 * 3.14 * raio

print (f"A área do círculo é igual a {area} e seu comprimento equivale a {comprimento}")

