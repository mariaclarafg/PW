# Peça ao usuário para digitar três números, armazene-os em variáveis e imprima a
# média aritmética dos três números.


numero_1 = int(input("Digite primero número: "))
numero_2 = int(input("Digite segundo número: "))
numero_3 = int(input("Digite terceiro número: "))

media_aritmética = (numero_1 + numero_2 + numero_3) / 3
print (f"A média aritmética é {media_aritmética}")