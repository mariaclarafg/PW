# Peça ao usuário para digitar a massa e a aceleração de um objeto e calcule a força
# resultante.

massa = float(input("Digite a massa do objeto(kg): "))
aceleracao = float(input("Digite a aceleração do objeto(m/s): "))

forca_resultando = massa * aceleracao

print(f"A força resultante do objeto é {forca_resultando}")