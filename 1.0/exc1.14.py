# Peça ao usuário para digitar a base e a altura de um triângulo e calcule a área desse
# triângulo.

base_triangulo = int(input("Digite o valor da base do triângulo: "))
altura_triangulo = int(input("Digite o valor da altura do triângulo: "))

area = (base_triangulo * altura_triangulo) / 2

print (f"A área do triângulo é {area}")